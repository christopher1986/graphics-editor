/**
 * The Dimension class maintains the width, height in pixels of an object.
 */
export default class Dimension {

  /**
   * Create a new Dimension instance.
   *
   * @param width - (optional) The width in pixels, defaults to 0.
   * @param height - (optional) The height in pixels, defaults to 0.
   */
  public constructor(public width: number = 0, public height: number = 0) {}

}
